        /// <returns>The <see cref="Task"/> that represents the asynchronous operation, containing the <see cref="IdentityResult"/> of the update operation.</returns>
        public async virtual Task<IdentityResult> UpdateAsync(TUser user, CancellationToken cancellationToken = default(CancellationToken))
        {
            cancellationToken.ThrowIfCancellationRequested();
            ThrowIfDisposed();
            if (user == null)
            {
                throw new ArgumentNullException(nameof(user));
            }
            user.ConcurrencyStamp = Guid.NewGuid().ToString();
            var filter = Builders<TUser>.Filter.Eq("_id", ObjectId.Parse(user.Id));
            await userCollection.ReplaceOneAsync(filter, user);
            return IdentityResult.Success;
        }

