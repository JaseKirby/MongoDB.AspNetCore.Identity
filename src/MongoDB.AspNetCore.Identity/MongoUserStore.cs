        /// <summary>
        ///     Gets the database from URL.
        /// </summary>
        /// <param name="url">The URL.</param>
        /// <returns>MongoDatabase.</returns>
        private IMongoDatabase GetDatabaseFromUrl(MongoUrl url)
        {
            var client = new MongoClient(url);
            if (url.DatabaseName == null)
            {
                throw new Exception("No database name specified in connection string");
            }
            return client.GetDatabase(url.DatabaseName);
        }

        /// <summary>
        ///     Uses connectionString to connect to server and then uses databae name specified.
        /// </summary>
        /// <param name="connectionString">The connection string.</param>
        /// <param name="dbName">Name of the database.</param>
        /// <returns>MongoDatabase.</returns>
        private IMongoDatabase GetDatabase(string connectionString, string dbName)
        {
            var client = new MongoClient(connectionString);
            return client.GetDatabase(dbName);
        }


        /// <summary>
        ///     Initializes a new instance of the <see cref="MongoUserStore{TUser}" /> class.
        ///     Takes a mongoDB conecction string with the database name included
        /// </summary>
        /// <param name="connectionString">The connection name or URL.</param>
        public MongoUserStore(string connectionString)
        {
            db = GetDatabaseFromUrl(new MongoUrl(connectionString));
            userCollection = db.GetCollection<TUser>(typeof(TUser).Name);
        }

